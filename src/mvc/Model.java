package mvc;

/**
 *
 * @author kylefrancis
 */
public class Model {
    private String name;
    private int age;
    
    public Model(String n, int a)
    {
        this.name = n;
        this.age = a;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
}
